/*program to print the elements of an array. */
class print_num 
{
    public static void main(String[] args)
    {
        int[] num={1,4,6,8,10};//declare and initialize an array of five integer values.
        
        //displaying the five integer values in an array.
        for (int i=0;i<num.length;i++)
        {
            System.out.println(num[i]);
        } 
    }   
}