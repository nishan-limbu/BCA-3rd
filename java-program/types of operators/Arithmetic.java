//add,subtract,multiply,divide,modulus operation in java program
class Arithmetic
{
	public static void main(String[] args)
	{
		int a=12;
		int b=6;
		int add;
		int sub;
		int mul;
		int div;
		int modu;
		add=(a + b);
		sub=(a - b);
		mul=(a * b);
		div=(a / b);
		modu=(a % b);
		System.out.println("ADD = "+add);
		System.out.println("Subtract = "+sub);
		System.out.println("Multiply = "+mul);
		System.out.println("Divide = " +div);
		System.out.println("Remainder = " +modu);

	}
}