//Ternary operations in java program
class Ternary
{
	public static void main(String[] args)
	{
		int a=90;
		int b=82;
		//displaying output using ternary operator.
		String result;
		result=(a==b) ? "a is equal b." : "a is not equal b.";
		//displaying the ouput using ternary operator;	
		System.out.println(result);
		
	}
}